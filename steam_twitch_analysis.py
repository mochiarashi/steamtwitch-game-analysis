# Keep only the variables we need
reg = df[[
    "steam_appid", "year_month", "year_month_dt",
    "log_avg_players", "log_avg_viewers_lag1"
] + controls].copy()

# Drop any rows with missing key variables
reg = reg.dropna(subset=["log_avg_players", "log_avg_viewers_lag1"])
print("After dropping missing key vars:", reg.shape)

# 2. MODEL 1: y ~ x  (no controls, no FE) - OLS with clustered SE

X1 = sm.add_constant(reg["log_avg_viewers_lag1"])
model1 = sm.OLS(reg["log_avg_players"], X1)
results1 = model1.fit(cov_type="cluster", cov_kwds={"groups": reg["steam_appid"]})

print("\n=== MODEL 1: y on x (OLS, clustered by game) ===\n")
print(results1.summary())

# 3. MODEL 2: y ~ x + controls  (no FE) - OLS with clustered SE

X2 = sm.add_constant(reg[["log_avg_viewers_lag1"] + controls])
model2 = sm.OLS(reg["log_avg_players"], X2)
results2 = model2.fit(cov_type="cluster", cov_kwds={"groups": reg["steam_appid"]})

print("\n=== MODEL 2: y on x + controls (OLS, clustered by game) ===\n")
