df["date_str"] = df["date"].dt.to_period("M").astype(str)

# Drop rows with missing key variables for IV estimation
key_vars = ["log_avg_players", "log_avg_viewers", "google_trend_lag1"]
df_reg = df.dropna(subset=key_vars).copy()

print("\nIV regression sample shape:", df_reg.shape)
print("Unique games in IV sample:", df_reg["game_clean"].nunique())
print("Unique months in IV sample:", df_reg["date_str"].nunique())


# 3. Summary statistics
summary_cols = [
    "log_avg_players",
    "log_avg_viewers",
    "log_peak_players",
    "gain_percent",
    "google_trend",
    "google_trend_lag1",
]

print("\n=== SUMMARY STATISTICS (IV sample) ===")
summary_table = df_reg[summary_cols].describe().T
print(summary_table)

# Optionally export summary stats table to CSV (for paper/appendix)
summary_table.to_csv("summary_stats_iv_sample.csv")
print("\nSaved summary stats to summary_stats_iv_sample.csv")


# 4. Baseline OLS with game and time fixed effects
# Dependent variable: log_avg_players
# Main regressor: log_avg_viewers (endogenous)
# Fixed effects: game_clean (game FE) and date_str (time FE)
ols_formula = "log_avg_players ~ log_avg_viewers + C(game_clean) + C(date_str)"

ols_model = smf.ols(
    ols_formula,
    data=df_reg
).fit(cov_type="cluster", cov_kwds={"groups": df_reg["game_clean"]})

print("\n=== BASELINE OLS: log_avg_players on log_avg_viewers (with FE, clustered by game) ===\n")
print(ols_model.summary())


# 5. First Stage (FSLS): log_avg_viewers ~ google_trend_lag1 + FE
# This is the first stage of the IV model.

fs_formula = "log_avg_viewers ~ google_trend_lag1 + C(game_clean) + C(date_str)"

fs_model = smf.ols(
    fs_formula,
    data=df_reg
).fit(cov_type="cluster", cov_kwds={"groups": df_reg["game_clean"]})

print("\n=== FIRST STAGE (FSLS): log_avg_viewers on google_trend_lag1 + FE ===\n")
print(fs_model.summary())



# 6. IV-2SLS main specification (TSLS)
# Second Stage:
# log_avg_players = alpha_i + gamma_t + beta * log_avg_viewers_hat + error
# where log_avg_viewers_hat is predicted from the first stage.
# FE: game_clean and date_str
iv_formula = "log_avg_players ~ 1 + [log_avg_viewers ~ google_trend_lag1] + C(game_clean) + C(date_str)"

iv_model = IV2SLS.from_formula(
    iv_formula,
    data=df_reg
).fit(cov_type="clustered", clusters=df_reg["game_clean"])

print("\n=== IV-2SLS RESULTS: log_avg_players on instrumented log_avg_viewers (with FE) ===\n")
print(iv_model.summary)

# - coefficient on log_avg_viewers (should be ~ 1.0)
# - standard error, t-stat, confidence interval


# 7. Reverse IV (for discussion, not main specification)
# swap the dependent and endogenous variables:
# log_avg_viewers = alpha_i + gamma_t + theta * log_avg_players_hat + error
# This is used to check whether we find evidence that players drive viewership.
reverse_iv_formula = "log_avg_viewers ~ 1 + [log_avg_players ~ google_trend_lag1] + C(game_clean) + C(date_str)"

reverse_iv_model = IV2SLS.from_formula(
    reverse_iv_formula,
    data=df_reg
).fit(cov_type="clustered", clusters=df_reg["game_clean"])

print("\n=== REVERSE IV RESULTS: log_avg_viewers on instrumented log_avg_players (with FE) ===\n")
print(reverse_iv_model.summary)


