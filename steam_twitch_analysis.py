# Initialize Google Trends connection
pytrends = TrendReq(hl='en-US', tz=360)

def fetch_trend_for_game(game_name,
                         start_date="2012-01-01",
                         end_date="2025-01-01",
                         sleep_sec=6):
    """
    Fetch monthly Google Trends index for a single game.
    Returns a DataFrame with columns: ['date', 'game_clean', 'google_trend'].
    """
    # Use the game name as search keyword; lower() can sometimes help matching
    kw = game_name.lower()

    # Build payload for this keyword
    timeframe = f"{start_date} {end_date}"
    pytrends.build_payload([kw], timeframe=timeframe, geo="")

    # Query interest over time
    df = pytrends.interest_over_time()

    # If no data returned, skip
    if df.empty:
        print(f"[NO DATA] {game_name}")
        time.sleep(sleep_sec)
        return None

    # Reset index so 'date' becomes a normal column
    df = df.reset_index()

    # Keep only date and the keyword column
    df = df[["date", kw]]

    # Rename the trend column to a generic name
    df = df.rename(columns={kw: "google_trend"})

    # Add game identifier
    df["game_clean"] = game_name

    print(f"[OK] {game_name} — {df.shape[0]} rows")

    # Be kind to Google and sleep a bit
    time.sleep(sleep_sec)

    return df

